using System;
namespace Encapsulation
{
    internal class Dosen
    {
        public static int jmlDosen = 0;
        public string nama;
        public int nik;

        public Dosen(string nama, int nik)
        {
            this.nama = nama;
            this.nik = nik;
            jmlDosen++;
        }

        public static int totalDosen
        {
            get { return jmlDosen; }
            set { jmlDosen = value; }
        }
        public string namaDosen
        {
            get { return nama; }
            set { nama = value; }
        }
        public int nikDosen
        {
            get { return nik; }
            set { nik = value; }
        }
    }
}

